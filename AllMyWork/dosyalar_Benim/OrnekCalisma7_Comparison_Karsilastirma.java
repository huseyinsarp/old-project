package dosyalar_Benim;

public class OrnekCalisma7_Comparison_Karsilastirma {
    public static void main(String[] args) {

        /* bu karşılaştırma durumunda sistem bize sonucu true / false olarak verir.
        >  bu oparatörler koşullu durumlarda kullanılır. Mesela Ahmetin yaşı 18 den büyükse oy kullansın
        <  18 den küçükse oy kullanmasın. Eğer Ahmetin yaşı 18e eşit yada büyükse Ahmet oy kullanabilir.
        >=
        <=
        ==
        !=
         */

        int sayi1 =100;
        int sayi2 =90;
        System.out.println(sayi1 > sayi2);
        System.out.println(sayi1 < sayi2);
        System.out.println(sayi1 >= sayi2);
        System.out.println(sayi1 <= sayi2);
        System.out.println(sayi1 == sayi2);
        System.out.println(sayi1 != sayi2);


    }

}
